const mongoose = require('mongoose');

const BlogSection = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    mainImage: {
        type: String,
        // required: true
    },
    sections: {
        type: mongoose.Schema.Types.Mixed,
    },




},
    { timestamps: true });

const Blogs = mongoose.model('BlogSection', BlogSection);

module.exports = Blogs;
